async function main() {
  const [deployer] = await ethers.getSigners();

  console.log("Deploying contracts with the account:", deployer.address);

  console.log("Account balance:", (await deployer.getBalance()).toString());

  const WeatherRecord = await ethers.getContractFactory("WeatherRecord");
  const contract = await WeatherRecord.deploy();

  console.log("WeatherRecord address:", contract.address);
}

main()
  .then(() => process.exit(0))
  .catch((error) => {
    console.error(error);
    process.exit(1);
  });